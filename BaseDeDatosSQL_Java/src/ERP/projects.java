package ERP;

public class projects {

	private int id;
	private String nombre;
	private int horas;

	
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getHours() {
		return horas;
	}
	public void setHours(int horas) {
		this.horas = horas;
	}

	
}
